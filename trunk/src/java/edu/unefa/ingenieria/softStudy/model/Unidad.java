package edu.unefa.ingenieria.softStudy.model;

/**
 *
 * @author david
 */
public class Unidad {
    public String idUnidad;
    public String nombreUnidad;
    public String objetivoUnidad;

    public String getIdUnidad() {
        return idUnidad;
    }

    public void setIdUnidad(String idUnidad) {
        this.idUnidad = idUnidad;
    }

    public String getNombreUnidad() {
        return nombreUnidad;
    }

    public void setNombreUnidad(String nombreUnidad) {
        this.nombreUnidad = nombreUnidad;
    }

    public String getObjetivoUnidad() {
        return objetivoUnidad;
    }

    public void setObjetivoUnidad(String objetivoUnidad) {
        this.objetivoUnidad = objetivoUnidad;
    }
    
    
    
}
