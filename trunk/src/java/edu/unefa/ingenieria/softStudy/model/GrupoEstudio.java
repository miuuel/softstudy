package edu.unefa.ingenieria.softStudy.model;

/**
 *
 * @author david
 */
public class GrupoEstudio {
    public String idGrupoEstudio;
    public String codigoGrupoEstudio;
    public String seccionGrupoEstudio;
    public String estadoGrupoEstudio;
    public String fechaGrupoEstudio;
    public String descripcionGrupoEstudio;

    public String getIdGrupoEstudio() {
        return idGrupoEstudio;
    }

    public void setIdGrupoEstudio(String idGrupoEstudio) {
        this.idGrupoEstudio = idGrupoEstudio;
    }

    public String getCodigoGrupoEstudio() {
        return codigoGrupoEstudio;
    }

    public void setCodigoGrupoEstudio(String codigoGrupoEstudio) {
        this.codigoGrupoEstudio = codigoGrupoEstudio;
    }

    public String getSeccionGrupoEstudio() {
        return seccionGrupoEstudio;
    }

    public void setSeccionGrupoEstudio(String seccionGrupoEstudio) {
        this.seccionGrupoEstudio = seccionGrupoEstudio;
    }

    public String getEstadoGrupoEstudio() {
        return estadoGrupoEstudio;
    }

    public void setEstadoGrupoEstudio(String estadoGrupoEstudio) {
        this.estadoGrupoEstudio = estadoGrupoEstudio;
    }

    public String getFechaGrupoEstudio() {
        return fechaGrupoEstudio;
    }

    public void setFechaGrupoEstudio(String fechaGrupoEstudio) {
        this.fechaGrupoEstudio = fechaGrupoEstudio;
    }

    public String getDescripcionGrupoEstudio() {
        return descripcionGrupoEstudio;
    }

    public void setDescripcionGrupoEstudio(String descripcionGrupoEstudio) {
        this.descripcionGrupoEstudio = descripcionGrupoEstudio;
    }
    
}
