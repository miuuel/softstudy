public class Programa {
    public String idPrograma;
    public String nombreMateria;
    public String objetivoMateria;
    public String vigenciaMateria;
    public String turnoMateria;
    public String semestreMateria;
    public String codigoMateria;
    public String prelacionMateria;
    public String horaTeorica;
    public String horaPractica;
    public String horaLaboratorio;
    public String creditos;

    public String getIdPrograma() {
        return idPrograma;
    }

    public void setIdPrograma(String idPrograma) {
        this.idPrograma = idPrograma;
    }

    public String getNombreMateria() {
        return nombreMateria;
    }

    public void setNombreMateria(String nombreMateria) {
        this.nombreMateria = nombreMateria;
    }

    public String getObjetivoMateria() {
        return objetivoMateria;
    }

    public void setObjetivoMateria(String objetivoMateria) {
        this.objetivoMateria = objetivoMateria;
    }

    public String getVigenciaMateria() {
        return vigenciaMateria;
    }

    public void setVigenciaMateria(String vigenciaMateria) {
        this.vigenciaMateria = vigenciaMateria;
    }

    public String getTurnoMateria() {
        return turnoMateria;
    }

    public void setTurnoMateria(String turnoMateria) {
        this.turnoMateria = turnoMateria;
    }

    public String getSemestreMateria() {
        return semestreMateria;
    }

    public void setSemestreMateria(String semestreMateria) {
        this.semestreMateria = semestreMateria;
    }

    public String getCodigoMateria() {
        return codigoMateria;
    }

    public void setCodigoMateria(String codigoMateria) {
        this.codigoMateria = codigoMateria;
    }

    public String getPrelacionMateria() {
        return prelacionMateria;
    }

    public void setPrelacionMateria(String prelacionMateria) {
        this.prelacionMateria = prelacionMateria;
    }

    public String getHoraTeorica() {
        return horaTeorica;
    }

    public void setHoraTeorica(String horaTeorica) {
        this.horaTeorica = horaTeorica;
    }

    public String getHoraPractica() {
        return horaPractica;
    }

    public void setHoraPractica(String horaPractica) {
        this.horaPractica = horaPractica;
    }

    public String getHoraLaboratorio() {
        return horaLaboratorio;
    }

    public void setHoraLaboratorio(String horaLaboratorio) {
        this.horaLaboratorio = horaLaboratorio;
    }

    public String getCreditos() {
        return creditos;
    }

    public void setCreditos(String creditos) {
        this.creditos = creditos;
    }
    
}
