package edu.unefa.ingenieria.softStudy.model;

/**
 *
 * @author david
 */
public class Tema {
    public String idTema;
    public String estadoTema;
    public String fechaTema;
    public String descripcionTema;

    public String getIdTema() {
        return idTema;
    }

    public void setIdTema(String idTema) {
        this.idTema = idTema;
    }

    public String getEstadoTema() {
        return estadoTema;
    }

    public void setEstadoTema(String estadoTema) {
        this.estadoTema = estadoTema;
    }

    public String getFechaTema() {
        return fechaTema;
    }

    public void setFechaTema(String fechaTema) {
        this.fechaTema = fechaTema;
    }

    public String getDescripcionTema() {
        return descripcionTema;
    }

    public void setDescripcionTema(String descripcionTema) {
        this.descripcionTema = descripcionTema;
    }
     
    
}
